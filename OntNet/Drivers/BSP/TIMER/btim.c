#include "./BSP/LED/led.h"
#include "./BSP/TIMER/btim.h"
#include "./BSP/DHT11/dht11.h"
#include "onenet.h"


TIM_HandleTypeDef g_timx_handle;  /* 定时器句柄 */

/**
 * @brief       基本定时器TIMX定时中断初始化函数
 * @note
 *              基本定时器的时钟来自APB1,当PPRE1 ≥ 2分频的时候
 *              基本定时器的时钟为APB1时钟的2倍, 而APB1为36M, 所以定时器时钟 = 72Mhz
 *              定时器溢出时间计算方法: Tout = ((arr + 1) * (psc + 1)) / Ft us.
 *              Ft=定时器工作频率,单位:Mhz
 *
 * @param       arr: 自动重装值。
 * @param       psc: 时钟预分频数
 * @retval      无
 */
void btim_timx_int_init(uint16_t arr, uint16_t psc)
{
    g_timx_handle.Instance = BTIM_TIMX_INT;                      /* 通用定时器X */
    g_timx_handle.Init.Prescaler = psc;                          /* 设置预分频系数 */
    g_timx_handle.Init.CounterMode = TIM_COUNTERMODE_UP;         /* 递增计数模式 */
    g_timx_handle.Init.Period = arr;                             /* 自动装载值 */
    HAL_TIM_Base_Init(&g_timx_handle);

    HAL_TIM_Base_Start_IT(&g_timx_handle);    /* 使能定时器x及其更新中断 */
}

/**
 * @brief       定时器底层驱动，开启时钟，设置中断优先级
                此函数会被HAL_TIM_Base_Init()函数调用
 * @param       htim:定时器句柄
 * @retval      无
 */
void HAL_TIM_Base_MspInit(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == BTIM_TIMX_INT)
    {
        BTIM_TIMX_INT_CLK_ENABLE();                     /* 使能TIM时钟 */
        HAL_NVIC_SetPriority(BTIM_TIMX_INT_IRQn, 1, 3); /* 抢占1，子优先级3，组2 */
        HAL_NVIC_EnableIRQ(BTIM_TIMX_INT_IRQn);         /* 开启ITM3中断 */
    }
}

/**
 * @brief       定时器TIMX中断服务函数
 * @param       无
 * @retval      无
 */
void BTIM_TIMX_INT_IRQHandler(void)
{
    HAL_TIM_IRQHandler(&g_timx_handle); /* 定时器中断公共处理函数 */
}

int Time1;								/* 心跳时间，单位s */
int Time1_t = 0;						/* 心跳计数器，单位s */
int Time2;								/* 更新时间，单位s */
int Time2_t = 0;						/* 更新计数器，单位s */
int Time3;								/* DHT11等待时间，单位ms */
int Time3_t = 0;						/* DHT11等待计数器，单位ms */
int Time4;								/* TCP等待时间，单位ms */
int Time4_t = 0;						/* TCP等待计数器，单位ms */
uint8_t LED_t;							/* 前一次的LED数据 */
uint8_t humi, temp;						/* 当前的温湿度数据 */
uint8_t humi_t, temp_t;					/* 前一次的温湿度数据 */

//DHT11忙等
void DHT11_Wait(void)
{
	while(Time3_t);
	Time3_t = Time3;
}
//TCP忙等
void TCP_Wait(void)
{
	while(Time4_t);
	Time4_t = Time4;
}


/**
 * @brief       定时器更新中断回调函数
 * @param       htim:定时器句柄
 * @retval      无
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == BTIM_TIMX_INT)
    {
		Time1_t ++;
		Time2_t ++;
		if(Time3_t > 0)
			Time3_t --;
		if(Time4_t > 0)
			Time4_t --;
    }
}




